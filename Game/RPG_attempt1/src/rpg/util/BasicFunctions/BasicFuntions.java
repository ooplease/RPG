package rpg.util.BasicFunctions;

public class BasicFuntions {
	
	public boolean isOdd(int x){
		if(x%2==1)
			return true;
		else return false;
	}
	
	public boolean isEven(int x){
		if(x%2==0)
			return true;
		else return false;
	}
	
	public boolean exists(int x){
		if(x>0)
			return true;
		else
			return false;
	}

}
